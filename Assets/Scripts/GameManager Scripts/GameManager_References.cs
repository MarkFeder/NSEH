using UnityEngine;
using System.Collections;

namespace NSEH
{
    public class GameManager_References : MonoBehaviour {

        public string player1Tag;
        public static string _player1Tag;

        public string player2Tag;
        public static string _player2Tag;

        public static GameObject _player1;
        public static GameObject _player2;

        void OnEnable()
        {
            if (player1Tag == "")
            {
                Debug.LogWarning("Please type in the name of the player 1 tag in the GameManager_References"+
                    "slot in the inspector or else the NSEH systems will not work");
            }

            if (player2Tag == "")
            {
                Debug.LogWarning("Please type in the name of the player 2 tag in the GameManager_References" +
                    "slot in the inspector or else the NSEH systems will not work");
            }

            _player1Tag = player1Tag; 
            _player2Tag = player2Tag;

            _player1 = GameObject.FindGameObjectWithTag(_player1Tag);
            _player2 = GameObject.FindGameObjectWithTag(_player2Tag);
        }
    }
}

